public class Especialidades {

    private Areas cienciaTech, servicos, habilidades, cultura, esportes;


    public Especialidades() {
    }

    public Areas getCienciaTech() {
        return cienciaTech;
    }

    public void setCienciaTech(Areas cienciaTech) {
        this.cienciaTech = cienciaTech;
    }

    public Areas getServicos() {
        return servicos;
    }

    public void setServicos(Areas servicos) {
        this.servicos = servicos;
    }

    public Areas getHabilidades() {
        return habilidades;
    }

    public void setHabilidades(Areas habilidades) {
        this.habilidades = habilidades;
    }

    public Areas getCultura() {
        return cultura;
    }

    public void setCultura(Areas cultura) {
        this.cultura = cultura;
    }

    public Areas getEsportes() {
        return esportes;
    }

    public void setEsportes(Areas esportes) {
        this.esportes = esportes;
    }
}
